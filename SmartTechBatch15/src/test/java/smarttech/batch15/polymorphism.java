package smarttech.batch15;

public class polymorphism extends JavaParameterMethod {

	public static void main(String[] args) {
		// Class day 10 time 3:57h

		add("This is my string");

	}

	static void add() {
		System.out.println("This is example polymorphism of overriding");

	}

	static void add(String a) { // Class day 10 time 4:05h
		System.out.println(a);

	}
}
